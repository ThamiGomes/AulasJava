
public class Matematica {

	// declarando método para calcular a área com o parâmetro x (o lado)
		int calculararea(int x) { 
			return x * x; //retornará a área do quadrado
		}

		// declarando método para calcular a área com o parâmetro x e y (a base e a
		// altura)
		int calculararea(int x, int y) {

			return x * y;  //retornará a área do retângulo
		}

}