
public class Matematica {
	int somar(int x, int y) { //método somar com duas variáveis inteiras
		
		//int z = x + y; possibilidade de retorno
		//return z; possibilidade de retorno
		return x + y; //retorno da fórmula
	}

	int somar(int x, int y, int z) { //método somar usando tres variáveis inteiras
		
		return x + y + z; //retorno da fórmula
	}
	double somar(double x, double y) { //método somar usando duas variáveis double
		
		return x + y; //retorno da fórmula
	}
}
